SET @start_date = '2024-08-20';
SET @end_date = '2024-12-11';
INSERT INTO Attendance (course_id, student_id, day, status)
SELECT 
    c.course_id,
    s.student_id,
    DATE_ADD(@start_date, INTERVAL (FLOOR(RAND() * (DATEDIFF(@end_date, @start_date) + 1))) DAY) AS day,
    CASE 
        WHEN RAND() < 0.7 THEN 'Present'
        WHEN RAND() < 0.9 THEN 'Absent'
        WHEN RAND() < 0.95 THEN 'Late'
        ELSE 'Excused'
    END AS status
FROM 
    Courses c
JOIN 
    Enrollment e ON c.course_id = e.course_id
JOIN 
    Student s ON e.student_id = s.student_id
WHERE 
    e.status = 'Enrolled' AND 
    DAYOFWEEK(DATE_ADD(@start_date, INTERVAL (FLOOR(RAND() * (DATEDIFF(@end_date, @start_date) + 1))) DAY)) != 1;

