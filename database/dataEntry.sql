INSERT INTO Enrollment (course_id, student_id, status, grade)
SELECT 
    c.course_id,
    s.student_id,
    CASE 
        WHEN s.current_status IN ('Suspended', 'Graduated', 'Inactive') THEN 
            CASE 
                WHEN s.current_status = 'Suspended' THEN 'Withdrawn'
                WHEN s.current_status = 'Graduated' THEN 'Completed'
                ELSE 'Withdrawn'  
            END
        ELSE 'Enrolled'
    END AS status,
    CASE 
        WHEN s.current_status = 'Graduated' THEN NULL
        WHEN s.current_status = 'Suspended' THEN NULL
        WHEN s.current_status = 'Inactive' THEN NULL
        ELSE NULL
    END AS grade
FROM 
    Courses c
JOIN 
    Student s ON c.department_id = s.major
WHERE 
    s.current_status IN ('Active', 'Suspended', 'Graduated', 'Inactive');

