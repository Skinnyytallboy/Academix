-- Student data is entered as
INSERT INTO Student (student_id, name, dob, roll_no, semester, academic_year, current_status)
SELECT 
    user_id AS student_id,
    CONCAT(
        UPPER(LEFT(REGEXP_REPLACE(username, '[0-9]+$', ''), 1)),
        LOWER(SUBSTRING(REGEXP_REPLACE(username, '[0-9]+$', ''), 2))
    ) AS name,
    CASE 
        WHEN user_id % 5 = 1 THEN '2002-08-15'
        WHEN user_id % 5 = 2 THEN '2003-01-20'
        WHEN user_id % 5 = 3 THEN '2001-11-05'
        WHEN user_id % 5 = 4 THEN '2002-03-12'
        ELSE '2000-05-22'
    END AS dob,
    CONCAT('CS', LPAD(user_id, 3, '0')) AS roll_no,
    (user_id % 8) + 1 AS semester,
    CASE
        WHEN (user_id % 4) = 0 THEN '2019-09-01'
        WHEN (user_id % 4) = 1 THEN '2020-09-01'
        WHEN (user_id % 4) = 2 THEN '2021-09-01'
        ELSE '2022-09-01'
    END AS academic_year,
    CASE 
        WHEN (user_id % 8) = 0 THEN 'Graduated'
        ELSE 'Active'
    END AS current_status
FROM User
WHERE role = 'student';

